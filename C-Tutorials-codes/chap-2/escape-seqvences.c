#include<stdio.h>

int main(){
    printf("1. Newline: Hello \nWorld\n");
    printf("2. Horizontal Tab: Hello \tWorld\n");
    printf("3. Vertical Tab: Hello \v World\n");
    printf("4. Backslash: Hello \\ World\n");
    printf("5. Single quote: Hello \' World\n");
    printf("6. Double quote: Hello \" World\n");
    printf("7. Alarm: Hello \a World\n");
    printf("8. Question Mark: Hello \? World\n");


    return 0;
}
