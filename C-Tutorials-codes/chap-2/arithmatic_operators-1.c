#include<stdio.h>

int main(){
    int a = 5, b = 4;

    //addition
    int sum = a + b;

    //subtraction
    int sub = a - b;

    //multiplication
    int mult = a * b;

    //Division
    int div = a / b;

    //Remainder
    int rem = a % b;

    printf("The sum of a and b is: %d\n", sum);
    printf("The subtraction of a and b is: %d\n", sub);
    printf("The multiplication of a and b is: %d\n", mult);
    printf("The division of a and b is: %d\n", div);
    printf("The remainder of a and b is: %d\n", rem);



    return 0;
}
